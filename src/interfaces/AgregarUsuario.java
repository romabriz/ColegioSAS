    public void buscardni(){
        conexion = new ConexionBase();
        Connection base = conexion.getConnection();
        
        DefaultTableModel Modelo = new DefaultTableModel();
        Modelo.addColumn("DNI");
        Modelo.addColumn("Apellidos");
        Modelo.addColumn("Nombres");
        this.tpersona.setModel(Modelo);
        String[] fila = new String[3];
        try {
            Statement St = base.createStatement();
            ResultSet Resultado=St.executeQuery("SELECT IdPersona, DNI, Apellido, Nombre FROM persona WHERE DNI = '"+this.txtdni.getText()+"'");
            while (Resultado.next()){
                IdPersona=Resultado.getInt("IdPersona");
                fila[0]=Resultado.getString("DNI");
                fila[1]=Resultado.getString("Apellido");
                fila[2]=Resultado.getString("Nombre");
                Modelo.addRow(fila);
            }
            this.tpersona.setModel(Modelo);
            if(this.IdPersona==0){
                CargarPersona cper = new CargarPersona();
                cper.setVisible(true);
            }
        else {
        }
        } catch (SQLException ex){
            
        }
    }

    
