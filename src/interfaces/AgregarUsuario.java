    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        // TODO add your handling code here:
        if( !"".equals(txtusuario.getText()) && !"".equals(txtclave.getText()) && !"".equals(txtclaver.getText())){
            
        
        conexion = new ConexionBase();
        Connection base = conexion.getConnection();
        try {
                PreparedStatement pst = base.prepareStatement("INSERT INTO usuario ( rol, usuario, clave, idpersona) VALUES(?,?,?,?)");
                pst.setString(1, this.cbxrol.getSelectedItem().toString());
                pst.setString(2, txtusuario.getText());
                String clave = null;
        if(     this.txtclave.getText().equals(txtclaver.getText())){
                clave = txtclave.getText();
        }
        else {
              JOptionPane.showMessageDialog(null, "Contrase√±as no coinciden");
        }
                pst.setString(3, clave);
                pst.setInt(4, IdPersona);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Cargado correctamente");
                txtdni.setText("");
                txtusuario.setText("");
                txtclave.setText("");
                txtclaver.setText("");
                DefaultTableModel Modelo= new DefaultTableModel();
                this.tpersona.setModel(Modelo);

        }catch (SQLException ex){   
        }
        }
        else{
            JOptionPane.showMessageDialog(null, "Ingrese todos los valores");
        }
    }//GEN-LAST:event_btnagregarActionPerformed

